    /**
     * Acció del botó Modificar
     */
    private void buttonModifyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonModifyActionPerformed
        incidentToModify.setEndDate(TextDataFinal.getText());
        incidentToModify.setRoadName(textCarretera.getText());
        incidentToModify.setDescription(textDescripcio1.getText());
        incidentToModify.setGeo(textGeo.getText());
        incidentToModify.setTecnicId(Integer.parseInt(textIdTecnic.getText()));
        incidentToModify.setKM(textKM.getText());
        int incidentState = comboEstat.getSelectedIndex()+1;
        incidentToModify.setIncidentTypeId(incidentState);
        if(comboPrioritat.getSelectedIndex() == 0){
            incidentToModify.setUrgent(true);
        }
        else{
            incidentToModify.setUrgent(false);
        }
        
        Functions.modifyIncident(user, incidentToModify);
        this.dispose();
    }//GEN-LAST:event_buttonModifyActionPerformed

