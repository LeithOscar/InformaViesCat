    /**
     * Assigna valor a incidentToModify i inicialitza valors
     * @param incidentToModify incident a modificar.
     */
    public void setIncidentToModify(Incident incidentToModify) {
        this.incidentToModify = incidentToModify;
        
        this.TextDataFinal.setText(this.incidentToModify.getEndDate());
        this.textCarretera.setText(this.incidentToModify.getRoadName());
        this.textDataInici.setText(this.incidentToModify.getStartDate());
        this.textDescripcio1.setText(this.incidentToModify.getDescription());
        this.textGeo.setText(this.incidentToModify.getGeo());
        this.textIdIncidencia.setText(String.valueOf(this.incidentToModify.getId()));
        this.textIdTecnic.setText(String.valueOf(this.incidentToModify.getTecnicId()));
        this.textIdUser.setText(String.valueOf(this.incidentToModify.getUserId()));
        this.textKM.setText(this.incidentToModify.getKM());
        this.comboEstat.setSelectedIndex(this.incidentToModify.getIncidentTypeId()-1);
        if(this.incidentToModify.isUrgent()){
            this.comboPrioritat.setSelectedIndex(0);
        }
        else{
            this.comboPrioritat.setSelectedIndex(1);
        }
    }
